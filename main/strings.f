C     -------------------------------------------------------------------
C     PROGRAM TO UNDERSTAND STRING CONCATENATION
C     -------------------------------------------------------------------

      PROGRAM STRING

C     -------------------------------------------------------------------      
C     DECLARE VARIABLES      
C     -------------------------------------------------------------------      

      CHARACTER(LEN=30) :: A = "FERRO"
      CHARACTER(LEN=6) :: B = "PAREDES"
      CHARACTER(LEN=10) :: C = "FERRO"                     // "CARRILES"

C     -------------------------------------------------------------------      
C     WRITE VARIABLES TO THE SCREEN      
C     -------------------------------------------------------------------      
      WRITE(*,*) "A = ", A
      WRITE(*,*) "B = ", B
      WRITE(*,*) "C = ", C

C     -------------------------------------------------------------------      
C     END PROGRAM      
C     -------------------------------------------------------------------      
      END PROGRAM STRING